#!/usr/bin/env node

import { existsSync, mkdirSync, readFileSync, readdirSync, writeFileSync } from "node:fs";
import { basename, dirname, join, resolve } from "node:path";

const repoRoot = resolve(new URL("..", import.meta.url).pathname);
const canonicalSchemasDir = join(
	repoRoot,
	"packages",
	"pipeline_contracts",
	"pipeline_contracts",
	"schemas",
);
const outputPaths = [
	join(repoRoot, "packages", "pipeline_contracts", "typescript", "generated-schemas.ts"),
];
const checkMode = process.argv.includes("--check");

function camelCaseSchemaKey(filename) {
	const stem = filename.replace(/\.v1\.schema\.json$/, "").replace(/\.schema\.json$/, "");
	return stem
		.split(/[._-]+/)
		.filter(Boolean)
		.map((part, index) => {
			const lower = part.toLowerCase();
			if (index === 0) return lower;
			return `${lower.slice(0, 1).toUpperCase()}${lower.slice(1)}`;
		})
		.join("");
}

function listSchemaFiles() {
	return readdirSync(canonicalSchemasDir)
		.filter((name) => name.endsWith(".schema.json"))
		.sort();
}

function readJson(path) {
	return JSON.parse(readFileSync(path, "utf8"));
}

function stableJson(value) {
	return JSON.stringify(value, null, 2);
}

function generatedContents() {
	const entries = listSchemaFiles().map((filename) => {
		const schema = readJson(join(canonicalSchemasDir, filename));
		return {
			filename,
			key: camelCaseSchemaKey(filename),
			id: schema.$id,
			schema,
		};
	});
	const duplicateKey = entries.find(
		(entry, index) => entries.findIndex((candidate) => candidate.key === entry.key) !== index,
	);
	if (duplicateKey) {
		throw new Error(`duplicate generated schema key: ${duplicateKey.key}`);
	}
	const body = entries
		.map(
			(entry) =>
				`\t${entry.key}: ${stableJson(entry.schema).replace(/\n/g, "\n\t")} as const,`,
		)
		.join("\n");
	const manifest = entries
		.map(
			(entry) =>
				`\t{ key: ${JSON.stringify(entry.key)}, filename: ${JSON.stringify(entry.filename)}, id: ${JSON.stringify(entry.id)} },`,
		)
		.join("\n");
	return `/* This file is generated by scripts/generate-pipeline-contract-schemas.mjs. Do not edit by hand. */\n\nexport const generatedPipelineContractSchemas = {\n${body}\n} as const;\n\nexport const generatedPipelineContractSchemaManifest = [\n${manifest}\n] as const;\n`;
}

const contents = generatedContents();
const failures = [];

for (const outputPath of outputPaths) {
	if (checkMode) {
		if (!existsSync(outputPath)) {
			failures.push(`missing generated schema file: ${outputPath}`);
			continue;
		}
		const current = readFileSync(outputPath, "utf8");
		if (current !== contents) {
			failures.push(`generated schema file is stale: ${outputPath}`);
		}
		continue;
	}
	mkdirSync(dirname(outputPath), { recursive: true });
	writeFileSync(outputPath, contents, "utf8");
	console.log(`wrote ${basename(outputPath)}`);
}

if (failures.length > 0) {
	console.error("ERROR: generated pipeline contract schemas are stale.");
	for (const failure of failures) console.error(`- ${failure}`);
	console.error("Run: node scripts/generate-pipeline-contract-schemas.mjs");
	process.exit(1);
}

if (checkMode) {
	console.log("Generated pipeline contract schemas are current.");
}
