/**
 * Detect platform from URL. Returns platform ID or null.
 */
export function detectPlatformId(url: string): string | null {
	try {
		const hostname = new URL(url).hostname.toLowerCase().replace(/^www\./, "");
		if (DOMAIN_MAP[hostname]) return DOMAIN_MAP[hostname];
		// Subdomain check (e.g. username.substack.com)
		const parts = hostname.split(".");
		if (parts.length >= 2) {
			const base = parts.slice(-2).join(".");
			if (DOMAIN_MAP[base]) return DOMAIN_MAP[base];
		}
		return null;
	} catch {
		return null;
	}
}

/**
 * Generate a deep link URL for a given web URL and platform ID.
 */
export function generateDeepLink(
	url: string,
	platformId: string,
): string | null {
	const gen = DEEP_LINK_MAP[platformId];
	return gen ? gen(url) : null;
}

/**
 * Returns inline SVG HTML string for a platform icon.
 */
export function getPlatformSvg(platformId: string): string {
	const path = P[platformId] || P.globe;
	return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24" fill="currentColor" width="20" height="20"><path d="${path}"/></svg>`;
}

/**
 * Check if a string is a known platform ID.
 */
export function isKnownPlatform(id: string): boolean {
	return id in P && id !== "globe";
}
